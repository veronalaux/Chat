    private void jTextField3KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyPressed
// aqui vamos a enviar los mensajes a los clientes que se han conectado
        if (evt.getKeyCode()==10)//el 10 la tecla de enter 
        {
            //se envia el mensaje a los demas clientes que se han conectado
            cliente.enviarMensaje(jTextField3.getText());//notifco al cliente que he escrito un texto
            //una vez que que ha recibido un mensaje de otro cliente
            //lo va amostrar en el textarea
            jTextField3.setText(""); //limpio el textfield para escribir mas
        }
    }//GEN-LAST:event_jTextField3KeyPressed

    DefaultListModel dlm=new DefaultListModel();
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
        jList1.setModel(dlm);
        try{
            //instancia de tipo socket para la conexion
            int port = Integer.parseInt(jTextField1.getText()); //se configura el puerto y la URL->IP
            //estos parametros se obtienen del jTextField
            String url = jTextField2.getText();
            String nick = jTextField4.getText();
            if (cliente==null){
                cliente = new client(port,url,nick,this);
                cliente.start();  //empezamos a ejecutar el hilo 
                                   //siempre debemos poner que se debe ejecutar..no olvidar que si lo encendemos, tambien lo debemos apagar 
            }
            jButton1.setEnabled(false);
            jButton2.setEnabled(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error en los datos");
            cliente=null;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

